# Create two boolean variables x and y
x = True
y = False

# Logical AND
result_and = x and y

# Logical OR
result_or = x or y

# Logical NOT
result_not_x = not x
result_not_y = not y

# Print the results
print(f"x AND y: {result_and}")
print(f"x OR y: {result_or}")
print(f"NOT x: {result_not_x}")
print(f"NOT y: {result_not_y}")
